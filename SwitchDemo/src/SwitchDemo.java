
public class SwitchDemo {

	public static void main(String[] args) {
		int x = 30;
		switch (x) {

		case 1:
			System.out.println("Case 1");
			break;

		case 22:
			System.out.println("Case 2");
			break;

		case 30:
			System.out.println("Case 3");
			break;
			
		case 40:
			System.out.println("case 4");
			break;
			
		default:
			System.out.println("Default");
			
		}
	}

}
